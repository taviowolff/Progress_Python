'''
tipos de dados 
String - Str  ('Otávio', 'Wolff', 'Rio de Janeiro')
Inteiro - Int  (1, 2, 3, 4, 5)
Floating number - Float  (1.3 ; 0.5 ; 10.51)
Booleanos - Bool  (True / False)

Nesta aula aprendemos a utilizaro comendo 'type', para descobrir qual tipo é o dado representado na linha.
In this class you will learn how to use 'type', to find out what type is the data in the line.
'''

print('Luiz', type('Luiz'))  # Str

print(10, type(10))  # Int

print(25.23, type(25.23))  # Float

print('l' == 'L', type('l' == 'L'))  # Bool
