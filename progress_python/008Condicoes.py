"""
Condições IF, ELIF e ELSE 
Nessa aula você esá vendo a relação das condições e as linhas de códigos
As condições que devolvem um valor False sempre serão passadas adiante, e caso nenhuma delas seja verdadeira a ELSE aparece na tela
IF - primeira condição a ser observada
ELIF - condição secundária que pode ser repetida diversas vezes 
else - a última condição a ser analisada caso as outras sejam falsas
"""
if False:
    print('verdadeira')

elif False:
    print('falso')

elif True:
    print('certo')  # essa condição aparece na tela pois é a única que apresenta o valor True

else:
    print('olá')
    