# Conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool

print(int('1'), type(int('1'))) # aqui mostra o objeito str transformato para int e o tipo da classe do objeto
print(type(float('1') + 1)) # mostra qual o tipo da classe do objeto
print(float('1') + 1) # soma de float e o '1' de srt foi para float
print(str(1) + ('b')) # Concatenação de str e o 1 foi transformado em str